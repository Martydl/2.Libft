#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	int		j;
	char	*s3;

	if (!s1 && !s2)
		return (NULL);
	if (!s1)
		return (ft_strdup(s2));
	if (!s2)
		return (ft_strdup(s1));
	i = ft_strlen((char*)s1) + ft_strlen((char*)s2) + 1;
	if (!(s3 = (char*)malloc(sizeof(char) * i)))
		return (NULL);
	i = -1;
	while (s1[++i])
		s3[i] = s1[i];
	j = -1;
	while (s2[++j])
		s3[i++] = s2[j];
	s3[i] = '\0';
	return (s3);
}
